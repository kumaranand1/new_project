package exercise1;

public class Egg {
    public static void main(String[] args) {
        for (int i = 1; i <= 1; i++) {
            System.out.println("  _______");
        }
        for (int j = 1; j <= 1; j++) {
            System.out.println(" /       \\");// printing spaces in line2
        }
        for (int k = 1; k <= 1; k++) {
            System.out.println("/         \\"); // printing spaces in line3

        }

        for (int m = 1; m <= 1; m++) {
            System.out.println("-\"-'-\"-'-\"-");
        }

        for (int k = 1; k <= 1; k++) {
            System.out.println("\\         /"); // printing spaces in line5

        }
        for (int n = 1; n <= 1; n++) {
            System.out.println(" \\_______/"); // printing spaces in line6

        }

    }
}
